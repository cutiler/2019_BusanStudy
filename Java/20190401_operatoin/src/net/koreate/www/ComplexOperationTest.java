package net.koreate.www;

public class ComplexOperationTest {
	
	public static void main(String[] args) {
		
		int num1 = 5;
		int num2 = 10;
		
		num1 += num2;
		
		System.out.println(num1);
		
		num1 -= num2;
		
		System.out.println(num1);
		
		num1 *= num2;
		
		System.out.println(num1);
		
		num1 /= num2;
		
		System.out.println(num1);
		
		num1 %= num2;
		
		System.out.println(num1);
		
		
	}

}
